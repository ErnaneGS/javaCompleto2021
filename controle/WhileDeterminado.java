package controle;

public class WhileDeterminado {
	public static void main(String[] args) {
		//while repetição
		/*Enquanto a condição dentro do parenteses do while for verdadeira
		 * a operação vai ser executada
		 * Sempre será incrementado o contador
		 */
		
		int contador = 1;
		while(contador <= 10) {
			System.out.println("Olá Bom dia!");
			contador ++;
		}
	}
}
